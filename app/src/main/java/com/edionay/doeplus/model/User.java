package com.edionay.doeplus.model;

public class User {

    String name;
    String password;
    String email;

    public User() {

    }


    public void setName(String name) {
        this.name = name;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
